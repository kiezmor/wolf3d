#include "libft.h"

char	*ft_strmap(char const *s, char (*f)(char))
{
	char	*str;
	size_t	i;
	size_t	len;

	if (!(s))
		return (NULL);
	i = 0;
	len = ft_strlen(s) + 1;
	if (!(str = (char *)malloc(sizeof(*str) * (len))))
		return (NULL);
	while (s[i])
	{
		str[i] = f(s[i]);
		i++;
	}
	str[i] = '\0';
	return (str);
}
